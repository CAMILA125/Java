package exercicios;


public class Carro {
    private int valor_car;
    private String marca_car;
    private String modelo_car;


    public int getValor_car() {
        return valor_car;
    }

    public void setValor_car(int valor_car) {
        this.valor_car = valor_car;
    }

    public String getMarca_car() {
        return marca_car;
    }

    public void setMarca_car(String marca_car) {
        this.marca_car = marca_car;
    }

    public String getModelo_car() {
        return modelo_car;
    }

    public void setModelo_car(String modelo_car) {
        this.modelo_car = modelo_car;
    }
}
